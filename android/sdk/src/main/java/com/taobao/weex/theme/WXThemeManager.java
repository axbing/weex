    /**
     * Returns a new color which fits the current theme type.
     * @param type which type of color needs to be transformed.
     * @param c the color needed to be transformed.
     */ 
    public int getThemeColor(ThemeColorType type, int c) {
        int themeColor = c;
        WXColorThemeState theme = null;

        if (m_isNight) {
            theme = WXColorThemeStateNight.getInstance();
        } else  if (m_isTransparent) {
        } else if (m_themeType > 0) {
            theme = WXColorThemeStateDay.getInstance();
        }

        if (theme == null) {
            return c;
        }

        switch(type) {
            case BACKGROUND:
                themeColor = theme.getColorThemeBackground(c);
                break;
            case BIG_MUL_BACKGROUND:
                themeColor = theme.getColorThemeBigMulBackground(c);
                break;
            case MEDIUM_MUL_BACKGROUND:
                themeColor = theme.getColorThemeMediumMulBackground(c);
                break;
            case SMALL_MUL_BACKGROUND:
                themeColor = theme.getColorThemeSmallMulBackground(c);
                break;
            case POSITIONED_BACKGROUND:
                themeColor = theme.getColorThemePositionedBackground(c);
                break;
            case GRADIENT_BACKGROUND:
                themeColor = theme.getColorThemeGradientBackground(c);
                break;
            case VISITED_FONT:
                themeColor = theme.getColorThemeVisitedFont(c);
                break;
            case LINK_FONT:
                themeColor = theme.getColorThemeLinkedFont(c);
                break;
            case NORMAL_FONT:
                themeColor = theme.getColorThemeNormalFont(c);
                break;
            case BORDER:
                themeColor = theme.getColorThemeBorder(c);
            case CARET:
                themeColor = theme.getColorThemeCaret(c);
                break;
            default:
                break;
        }

        return themeColor;
    }

