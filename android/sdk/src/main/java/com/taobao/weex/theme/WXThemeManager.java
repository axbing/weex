    private void setWHITELIST(WXColorThemeParams obj, String key, String value, ColorTheme e)
            throws NumberFormatException {
        if (ColorTheme.KEY_FWLCOLOR != e)
            return;

        String[] rgbs = value.split(ColorTheme.DELIMITER_RGBS.toString());
        for (String rgbsItem: rgbs) {
            String[] rgb = rgbsItem.split(ColorTheme.DELIMITER_RGB.toString());
            if (3 == rgb.length) {
                int color = Color.rgb(Integer.parseInt(rgb[0]),
                        Integer.parseInt(rgb[1]),
                        Integer.parseInt(rgb[2]));
                if (!m_whiteListColorList.contains(color))
                    m_whiteListColorList.add(color);
            }
        }
    }

    private void setBLACKLIST(WXColorThemeParams obj, String key, String value, ColorTheme e)
            throws NumberFormatException {
        if (ColorTheme.KEY_FBLCOLOR != e)
            return;

        String[] rgbs = value.split(ColorTheme.DELIMITER_RGBS.toString());
        for (String rgbsItem: rgbs) {
            String[] rgb = rgbsItem.split(ColorTheme.DELIMITER_RGB.toString());
            if (3 == rgb.length) {
                int color = Color.rgb(Integer.parseInt(rgb[0]),
                        Integer.parseInt(rgb[1]),
                        Integer.parseInt(rgb[2]));
                if (!m_blackListColorList.contains(color))
                    m_blackListColorList.add(color);
            }
        }
    }

    /**
     * Clears all WXColorThemeParams objects.
     */
    protected void clearWXColorThemeParams() {
        Iterator<Integer> it = m_themeParamsMap.keySet().iterator();
        while (it.hasNext()) {
            it.remove();
        }
    }

    /**
     * Sets a WXColorThemeParams object with the specified type.
     * @param type the type of WXColorThemeParams.
     * @param obj the WXColorThemeParams object.
     */
    protected void setThemeParams(int type, WXColorThemeParams obj) {
        if (obj == null)
            return;

        m_themeParamsMap.put(type, obj);
    }
